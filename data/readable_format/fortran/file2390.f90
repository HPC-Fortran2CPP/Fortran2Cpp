PROGRAM TestPAKDSP
    IMPLICIT NONE

    REAL PAK
    CHARACTER*1 SGNA
    INTEGER DEGS, MINS
    REAL SECS

    ! Test case: PAK = -123456789.0
    PAK = -123456789.0
    CALL PAKDSP(PAK, SGNA, DEGS, MINS, SECS)

    PRINT *, 'Test case: PAK = ', PAK
    PRINT *, 'SGNA: ', SGNA
    PRINT *, 'DEGS: ', DEGS
    PRINT *, 'MINS: ', MINS
    PRINT *, 'SECS: ', SECS

END PROGRAM TestPAKDSP

SUBROUTINE PAKDSP (PAK, SGNA, DEGS, MINS, SECS)
    IMPLICIT REAL (A-Z)
    REAL SECS
    INTEGER DEGS, MINS
    CHARACTER*1 SGNA, IBLANK, NEG
    DATA ZERO, CON1, CON2 /0.0E0, 1000000.0E0, 1000.0E0/
    DATA TOL /1.0E-4/
    DATA IBLANK, NEG /' ', '-'/
    SGNA = IBLANK
    IF (PAK .LT. ZERO) SGNA = NEG
    CON = ABS (PAK)
    DEGS = INT ((CON / CON1) + TOL)
    CON = MOD(CON, CON1)
    MINS = INT ((CON / CON2) + TOL)
    SECS = MOD(CON, CON2)
    RETURN
END SUBROUTINE PAKDSP