MODULE commons
  IMPLICIT NONE
CONTAINS
  SUBROUTINE MYCPU_TIME(POO)
    DOUBLE PRECISION MYTIME, POO
    CALL CPU_TIME(MYTIME)
    POO = MYTIME 
  END SUBROUTINE MYCPU_TIME
END MODULE commons

PROGRAM test_mycpu_time
  USE commons
  IMPLICIT NONE
  DOUBLE PRECISION poo

  CALL MYCPU_TIME(poo)
  PRINT *, "CPU Time: ", poo
END PROGRAM test_mycpu_time