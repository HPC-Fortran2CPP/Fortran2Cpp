PROGRAM test_dcabs1
  COMPLEX*16 Z
  DOUBLE PRECISION DCABS1
  EXTERNAL DCABS1

  Z = (3.0D0, 4.0D0)
  PRINT *, 'DCABS1((3,4)) = ', DCABS1(Z)

  Z = (-1.5D0, 2.5D0)
  PRINT *, 'DCABS1((-1.5,2.5)) = ', DCABS1(Z)

  Z = (0.0D0, 0.0D0)
  PRINT *, 'DCABS1((0,0)) = ', DCABS1(Z)

END PROGRAM test_dcabs1

DOUBLE PRECISION FUNCTION DCABS1(Z)
  COMPLEX*16 Z
  INTRINSIC ABS,DBLE,DIMAG
  DCABS1 = ABS(DBLE(Z)) + ABS(DIMAG(Z))
  RETURN
END