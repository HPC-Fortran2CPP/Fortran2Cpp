MODULE GET_LRBT_SLICES_mod
  IMPLICIT NONE
CONTAINS
  SUBROUTINE GET_LRBT_SLICES(ICHUNK, ILEFT, IRIGHT)
    INTEGER, INTENT(IN) :: ICHUNK
    INTEGER, INTENT(OUT) :: ILEFT, IRIGHT
    ! Example logic for demonstration
    ILEFT = ICHUNK - 1
    IRIGHT = ICHUNK + 1
  END SUBROUTINE GET_LRBT_SLICES
END MODULE GET_LRBT_SLICES_mod

PROGRAM main
  USE GET_LRBT_SLICES_mod
  IMPLICIT NONE
  INTEGER :: ICHUNK, ILEFT, IRIGHT

  ! Example input
  ICHUNK = 5

  CALL GET_LRBT_SLICES(ICHUNK, ILEFT, IRIGHT)

  PRINT *, 'ICHUNK:', ICHUNK
  PRINT *, 'ILEFT:', ILEFT
  PRINT *, 'IRIGHT:', IRIGHT
END PROGRAM main