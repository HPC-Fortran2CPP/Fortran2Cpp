#include <iostream>
#include <string>
#include <algorithm>

using namespace std;

int main() {
    int n;
    cin >> n;
    int num1 = 0, num2 = 0;
    for (int i = 1; i <= n; i++) {
        string w = to_string(i);
        string p = w;
        reverse(p.begin(), p.end());
        if (w == p) {
            if (i % 2 == 1) {
                num1++;
            } else {
                num2++;
            }
        }
    }
    cout << num2 << " " << num1 << endl;
    return 0;
}

