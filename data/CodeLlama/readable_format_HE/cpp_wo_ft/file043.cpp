#include <iostream>
#include <string>

using namespace std;

string remove_vowels(string text) {
    string vowels = "AEIOUaeiou";
    string result = "";
    for (int i = 0; i < text.length(); i++) {
        bool is_vowel = false;
        for (int j = 0; j < vowels.length(); j++) {
            if (text[i] == vowels[j]) {
                is_vowel = true;
                break;
            }
        }
        if (!is_vowel) {
            result += text[i];
        }
    }
    return result;
}

int main() {
    string text = "abcdef//nghijklm";
    string result = remove_vowels(text);
    cout << "Result: " << result << endl;
    return 0;
}

